package com.hpkj.share.vo;

public class ShareDetailVo {
	private String shareTitle;
	private String shareTime;
	private String authorName;
	private String authorComp;
	private String authroDepart;
	private String shareFileName;
	private String shareRealFileName;
	private String content;
	public String getShareTitle() {
		return shareTitle;
	}
	public void setShareTitle(String shareTitle) {
		this.shareTitle = shareTitle;
	}
	public String getShareTime() {
		return shareTime;
	}
	public void setShareTime(String shareTime) {
		this.shareTime = shareTime;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public String getAuthorComp() {
		return authorComp;
	}
	public void setAuthorComp(String authorComp) {
		this.authorComp = authorComp;
	}
	public String getAuthroDepart() {
		return authroDepart;
	}
	public void setAuthroDepart(String authroDepart) {
		this.authroDepart = authroDepart;
	}
	public String getShareFileName() {
		return shareFileName;
	}
	public void setShareFileName(String shareFileName) {
		this.shareFileName = shareFileName;
	}
	public String getShareRealFileName() {
		return shareRealFileName;
	}
	public void setShareRealFileName(String shareRealFileName) {
		this.shareRealFileName = shareRealFileName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
}
